names = ['pratik','sagar','prabin','sujan','sabin']
print(names) # prints as same as the list ['pratik','sagar','prabin','sujan','sabin']


print(names[0]) # prints the first element of the list
print(names[-1]) # prints the last element of the list
print(names[1:]) # prints the elements from the second element to the last element of the list